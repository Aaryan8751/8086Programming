DATA SEGMENT
    STRING1 DB 23H,25H,26H,25H,23H
    PALL DB 00H
    
    DATA ENDS

EXTRA SEGMENT
    STRING2 DB 05H DUP(?)
    
    EXTRA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,ES:EXTRA
    
    START:
    MOV AX,DATA
    MOV DS,AX
    MOV AX,EXTRA
    MOV ES,AX
    
    LEA SI,STRING1
    LEA DI,STRING2+04H
    MOV CX,05H
    
    BACK:
    CLD
    LODSB
    STD
    STOSB
    
    LOOP BACK
    
    LEA SI,STRING1
    LEA DI,STRING2
    MOV CX,05H
    REPE CMPSB
    JNZ SKIP
    INC PALL
    
    SKIP:INT 03H
    CODE ENDS
END START